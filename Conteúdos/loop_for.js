// LOOP FOR

for (let i = 0; i < 5; i++) {
    console.log('Estou aprendendo!', i);
}

for (let i = 1; i <= 5; i++) {
    if (i % 2 !== 0) { //Módulo
        console.log(i);
    }
}

for (let i = 5; i >= 1; i--) {
    if (i % 2 !== 0) { //Módulo
        console.log(i);
    }
}